// == vs ===

let x = "5";
let y = 5;
console.log(x == y); //true
console.log(x === y);//false
// In case of == :
// It check only value not datatype

// In case of ===:
// It check for both datatype as well as value

// != vs !==
let i = 1;
let j = "1";

console.log(i!=j);//false
// In case of !=:
// It check only value not datatype

console.log(i!==j);
// In case of !==:
// It check for both datatype as well as value

